package com.inheritance.multilevel;

public class Student {
	protected int studentRollNo;
	protected String studentName;
	protected double studentFee;
	protected String studentAddress;

	public Student(int studentRollNo, String studentName, double studentFee, String studentAddress) {
		super();
		this.studentRollNo = studentRollNo;
		this.studentName = studentName;
		this.studentFee = studentFee;
		this.studentAddress = studentAddress;
	}

	
}
